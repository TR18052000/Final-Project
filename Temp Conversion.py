# Temperature Converter from Celsius to Fahrenheit

Celsius=float(input("Enter temperature in celsius:")) #  The user emterd the desired temperature in celsius

Fahrenheit=(Celsius * 1.8)+32  # (°C × 9/5) + 32 = °F  °C-->Temperature in celsius  °F-->Temperature in Fahrenheit

# The above formula is applied to convert the degree Celsius to degree fahrenheit                                                      

print("Temperature in fahrenheit:",Fahrenheit) # The Temperature converted from degree Celsius to degree Fahrenheit is displayed

